# Strings in Python
# Concatenation (Addition of Strings)
# Strings can also be multiplied with integers
# f-strings, String Splicing
first = "Dev"
second = "Soc"
full_name = first + second # concatenation
print(full_name)

# talk about f strings

print(full_name.upper())
print(full_name.lower())

print(f"Name is {full_name}")